/* Printing Table from 2 to 10 in a single console */
#include<iostream>
using namespace std;
int main()
{
    int i;
    int a=2, b=3, c=4, d=5, e=6, f=7, g=8, h=9, j=10;
    for(i=1;i<=10;i++)                                  /* i will start from 1 to 10 using for loop */
    {
        cout<<a*i<<b*i<<c*i<<d*i<<e*i<<f*i<<g*i<<h*i<<j*i<<endl;
    }
return 0;
}
